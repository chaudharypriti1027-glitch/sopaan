const EN_DIR = path.join(ROOT, 'src/i18n/locales/en');
const GU_DIR = path.join(ROOT, 'src/i18n/locales/gu');

function readJson(name) {
  return JSON.parse(fs.readFileSync(path.join(EN_DIR, name), 'utf8'));
}

function writeJson(name, data) {
  fs.writeFileSync(path.join(GU_DIR, name), `${JSON.stringify(data, null, 2)}\n`);
}

